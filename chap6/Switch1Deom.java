package chap6;

public class Switch1Deom {

	public static void main(String[] args) {
		int number = 3;

		switch (number) {
		case 3:
			System.out.println("*");

			break;
		case 2:
			System.out.println("*");
		case 1:
			System.out.println("*");

		default:
			System.out.println("&");

		}

	}

}
